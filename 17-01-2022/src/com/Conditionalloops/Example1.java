package com.Conditionalloops;

import java.util.Scanner;

public class Example1 
{
				//for loop
	public static void main(String[] args) 
	{
     
		/**
		 * for(initialization; condition; increment/decrement statement) 
		 * { 
		 * 		//print values 
		 * }
		 * 
		 */
		
		for(int i = 5; i <= 10; i++)
		{
			System.out.println(i);
		}
		
		
		
	}

}
