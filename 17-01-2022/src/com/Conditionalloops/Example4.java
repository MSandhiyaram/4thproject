package com.Conditionalloops;

import java.util.Scanner;

public class Example4 
{
				//while loop
		public static void main(String[] args)
		{

			/**
			 * 
			 * initialization;
			 * 
			 * while(condition
			 * {
			 * 		//print statement
			 * 		//increment/decrement statement
			 * }
			 * 
			 */

			//initialization;
			int num = 1;

			//condition
			while(num <= 10)
			{
				System.out.println(num);
				num++;
			}
		
		
		
	}

}
