package com.Conditionalloops;

import java.util.Scanner;

public class Example5 
{
		
						//do-while loop
			public static void main(String[] args)
			{

				/**
				 * 
				 * initialization;
				 * 
				 * do
				 * {
				 * 		//print statement
				 * 		//increment/decrement statement
				 * }
				 * while(condition);
				 * 
				 */

				//initialization;
				int a = 1;

				//condition
				do
				{
					System.out.println(a);
					a++;
				}
				while(a <= -5);
				
		
	}

}
