package com.JumpstatementWithLoop;

import java.util.Scanner;

public class Example2 {

				//break statement
		public static void main(String[] args) 
		{
			
			      //break statement
			
				
				for(int i = 1; i <= 10; i++)
				{
					
					if(i == 5)
					{
						break;
					}
					
					System.out.println(i);
				}
				
	}

}
