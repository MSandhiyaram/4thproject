package com.JumpstatementWithLoop;

import java.util.Scanner;

public class Example1 {

					//break statement
		public static void main(String[] args) 
		{
			
			for(int i = 3; i <= 10; i++)
			{
				
				if(i == 7)
				{
					break;
				}
				
				System.out.println(i);
			}
		
	}

}
